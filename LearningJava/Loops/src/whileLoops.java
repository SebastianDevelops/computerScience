public class whileLoops {
    public static void main(String[] args)
    {
        /*
        Syntax:
        while(condition){
            //body
        }
        */

        // Q: Print numbers from 1 to 5
        int num = 1;

        while(num <= 5)
        {
            System.out.println(num);
            num++;            
        }
    }

}
